namespace CapsuleWars.Core
{
    /// <summary>
    /// The 8 fixed equipment slots on a unit. Hand slots can hold weapons,
    /// shields, or off-hand items; the others hold armor/cosmetic pieces.
    /// </summary>
    public enum EquipmentSlot
    {
        Helmet = 0,
        Shoulders = 1,
        Back = 2,
        Chest = 3,
        Arms = 4,
        Legs = 5,
        RightHand = 6,
        LeftHand = 7
    }
}
